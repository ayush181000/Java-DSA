public class MinNoOfDelAndInsertions {

}

class Solution {
    public static int canYouMake(String str, String ptr) {
        return str.length() + ptr.length() - (2 * lcs(str, ptr));
    }

    public static int lcs(String str, String t) {
        int n = str.length();
        int m = t.length();
        int dp[][] = new int[n + 1][m + 1];

        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= m; j++) {
                if (str.charAt(i - 1) == t.charAt(j - 1)) {
                    dp[i][j] = 1 + dp[i - 1][j - 1];
                } else {
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
                }
            }
        }

        return dp[n][m];
    }

}